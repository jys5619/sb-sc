WITH COLINFO AS (
   SELECT PDBMS_COLUMN AS DBMS_COLUMN
        , PCOLUMN_ALIAS || '(' || PCLASS_NAME || ')' AS COLUMN_ALIAS
     FROM PSYSATTRREF
    WHERE PDBMS_TABLE = LOWER(?)
      AND PCLASS_NAME = PDEFINED_CLASS_NAME
   GROUP BY PDBMS_COLUMN, PCOLUMN_ALIAS, PCLASS_NAME
)
SELECT DBMS_COLUMN
     , LISTAGG(COLUMN_ALIAS, ', ') WITHIN GROUP(ORDER BY DBMS_COLUMN) AS COMMENTS
  FROM COLINFO
 GROUP BY DBMS_COLUMN